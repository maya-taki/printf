#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*arr;
	size_t	check;

	if (nmemb == 0 || size == 0)
		return (malloc(1));
	check = nmemb * size;
	if (nmemb != check / size)
		return (NULL);
	arr = malloc(nmemb * size);
	if (!arr)
		return (NULL);
	ft_bzero(arr, nmemb * size);
	return (arr);
}
